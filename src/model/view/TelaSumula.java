    public TelaSumula(Partida partida) {
        initComponents();
        this.partida=partida;
        lblEstadio.setText(partida.getLocal());
        DateFormat sql = new SimpleDateFormat("yyyy-MM-dd");
        DateFormat normal = new SimpleDateFormat("dd-MM-yyyy");
        Date data= null;
        try {
            data = sql.parse(partida.getData());
        } catch (ParseException ex) {
            Logger.getLogger(TelaSumula.class.getName()).log(Level.SEVERE, null, ex);
        }
        lblData.setText(normal.format(data));
        lblMandante.setText(partida.getMandante().getNome());
        lblVisitante.setText(partida.getVisitante().getNome());
        lblGolsMandante.setText(String.valueOf(partida.getPlacarMandante()));
        lblGolsVisitante.setText(String.valueOf(partida.getPlacarVisitante()));
        if(partida.getPenaltisMandante() !=0 || partida.getPenaltisVisitante()!=0){
            lblPenaltiMandante.setText(String.valueOf(partida.getPenaltisMandante()));
            lblPenaltiVisitante.setText(String.valueOf(partida.getPenaltisVisitante()));
        }else{
            lblPenaltiMandante.setVisible(false);
            lblPenaltiVisitante.setVisible(false);
        }
        GolsDAO golDAO = new GolsDAO();
        CAmareloDAO amareloDAO = new CAmareloDAO();
        CVermelhoDAO vermelhoDAO = new CVermelhoDAO();
        List<Gols> gols = golDAO.listar(partida);
        List<CAmarelo> amarelos = amareloDAO.listar(partida);
        List<CVermelho> vermelhos = vermelhoDAO.listar(partida);
        
        DefaultTableModel modGols = (DefaultTableModel)tbGols.getModel();
        DefaultTableModel modAmarelo = (DefaultTableModel)tbAmarelos.getModel();
        DefaultTableModel modVermelho = (DefaultTableModel)tbVermelhos.getModel();
        
        for(Gols gol : gols){
            modGols.addRow(new Object[]{gol.getJogador().getApelido(),gol.getTime().getNome()});
        }
        for(CAmarelo amarelo:amarelos){
            modAmarelo.addRow(new Object[]{amarelo.getJogador().getApelido(),amarelo.getTime().getNome()});
        }
        for(CVermelho vermelho:vermelhos){
            modVermelho.addRow(new Object[]{vermelho.getJogador().getNome(),vermelho.getTime().getNome()});
        }
    }

