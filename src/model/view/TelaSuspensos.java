    public TelaSuspensos(Campeonato camp) {
        initComponents();
        this.camp = camp;

        DefaultTableModel tbm = (DefaultTableModel) tbSuspensos.getModel();
        JogadorDAO jogadorDAO = new JogadorDAO();
        TimeDAO timeDAO = new TimeDAO();
        List<Jogador> jogadores = jogadorDAO.listarSuspensosCamp(camp); // busca todos os jogadores que t√£o suspensos
        for (Jogador jogador : jogadores) {
            tbm.addRow(
                    new Object[]{
                        jogador.getApelido(), // coloca o apelido
                        timeDAO.buscar(jogador.getTime().getId()).getNome() // e o time que ele joga
                    });
        }
    }

