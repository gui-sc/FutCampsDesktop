    public TelaQuatroGrupos(Campeonato camp) {
        initComponents();
        this.camp = camp;
        DefaultTableModel tbm1 = (DefaultTableModel) tbGrupo1.getModel();
        DefaultTableModel tbm2 = (DefaultTableModel) tbGrupo2.getModel();
        DefaultTableModel tbm3 = (DefaultTableModel) tbGrupo3.getModel();
        DefaultTableModel tbm4 = (DefaultTableModel) tbGrupo4.getModel();
        TimeDAO timeDAO = new TimeDAO();
        if (camp.isIniciado()==false) {
            List<Time> cabecasDeChave = timeDAO.listarCabecasDeChave(camp);
            for (Time time : cabecasDeChave) {
                if (time.getGrupo() == 1) {
                    tbm1.addRow(new Object[]{
                        time.getNome(),
                        time.getPontos()
                    });
                } else if (time.getGrupo() == 2) {
                    tbm2.addRow(new Object[]{
                        time.getNome(),
                        time.getPontos()
                    });
                } else if (time.getGrupo() == 3) {
                    tbm3.addRow(new Object[]{
                        time.getNome(),
                        time.getPontos()
                    });
                } else {
                    tbm4.addRow(new Object[]{
                        time.getNome(),
                        time.getPontos()
                    });
                }
            }
            List<Time> times1 = timeDAO.listarGrupoSemCC(1, camp); // busca os times pelo n√∫mero do grupo por ordem de pontos
            for (Time time : times1) {// e coloca na tabela
                tbm1.addRow(
                        new Object[]{
                            time.getNome(),
                            time.getPontos()
                        }
                );
            }
            List<Time> times2 = timeDAO.listarGrupoSemCC(2, camp);
            for (Time time : times2) {
                tbm2.addRow(
                        new Object[]{
                            time.getNome(),
                            time.getPontos()
                        }
                );
            }
            List<Time> times3 = timeDAO.listarGrupoSemCC(3, camp);
            for (Time time : times3) {
                tbm3.addRow(
                        new Object[]{
                            time.getNome(),
                            time.getPontos()
                        }
                );
            }
            List<Time> times4 = timeDAO.listarGrupoSemCC(4, camp);
            for (Time time : times4) {
                tbm4.addRow(
                        new Object[]{
                            time.getNome(),
                            time.getPontos()
                        }
                );
            }
        }else{
            List<Time>time1 = timeDAO.listarGrupoComCC(1, camp);
            List<Time>time2 = timeDAO.listarGrupoComCC(2, camp);
            List<Time>time3 = timeDAO.listarGrupoComCC(3, camp);
            List<Time>time4 = timeDAO.listarGrupoComCC(4, camp);
            for(Time time : time1){
                tbm1.addRow(new Object[]{time.getNome(),time.getPontos()});
            }
            for(Time time : time2){
                tbm2.addRow(new Object[]{time.getNome(),time.getPontos()});
            }
            for(Time time : time3){
                tbm3.addRow(new Object[]{time.getNome(),time.getPontos()});
            }
            for(Time time : time4){
                tbm4.addRow(new Object[]{time.getNome(),time.getPontos()});
            }
        }
    }

