    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        txtNome.setBorder(bordaNormal);
        txtDirigente.setBorder(bordaNormal);
        txtCidade.setBorder(bordaNormal);
        if (txtNome.getText().equals("") || txtDirigente.getText().equals("") || txtCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Verifique os campos em branco");
            if (txtNome.getText().equals("")) {
                txtNome.setBorder(bordaVermelha);
            }
            if (txtDirigente.getText().equals("")) {
                txtDirigente.setBorder(bordaVermelha);
            }
            if (txtCidade.getText().equals("")) {
                txtCidade.setBorder(bordaVermelha);
            }
        } else {
            time.setNome(txtNome.getText());
            time.setDirigente(txtDirigente.getText());
            time.setCidade(txtCidade.getText());
            TimeDAO timeDAO = new TimeDAO();
            timeDAO.alterar(time); //troca os dados antigos pelos novos
            TelaEditarCamp tec = new TelaEditarCamp(camp);
            tec.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tbJogadores.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um jogador");
        } else {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza?", "Excluir Jogador", JOptionPane.WARNING_MESSAGE) == 0) {
                JogadorDAO jogadorDAO = new JogadorDAO();
                Object documento = tbJogadores.getValueAt(tbJogadores.getSelectedRow(), 0); //pega o documento do jogador selecionado
                int cod = (int) documento; // converte em inteiro
                jogadorDAO.excluir(cod); // exclui o jogador do banco de dados
                DefaultTableModel tbm = (DefaultTableModel) tbJogadores.getModel();
                tbm.removeRow(tbJogadores.getSelectedRow()); //remove da lista
            } else {

            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (tbJogadores.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um jogador");
        } else {
            JogadorDAO jogadorDAO = new JogadorDAO();
            Object documento = tbJogadores.getValueAt(tbJogadores.getSelectedRow(), 0);

            Jogador jogador = jogadorDAO.buscar((int) documento);
            acao = "alterar"; //fala pra outra tela o que Ã© pra fazer
            TelaAlteraJogador taj = new TelaAlteraJogador(camp, time, jogador, acao);
            taj.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

