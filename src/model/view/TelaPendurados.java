    public TelaPendurados(Campeonato camp) {
        initComponents();
        this.camp = camp;

        DefaultTableModel tbm = (DefaultTableModel) tbPendurados.getModel();
        JogadorDAO jogadorDAO = new JogadorDAO();
        TimeDAO timeDAO = new TimeDAO();
        Time time = new Time();
        List<Jogador> jogadores = jogadorDAO.listarPendurados(camp); // busca todos os jogadores que est√£o pendurados
        for (Jogador jogador : jogadores) {
            tbm.addRow(
                    new Object[]{
                        jogador.getApelido(),
                        timeDAO.buscar(jogador.getTime().getId()).getNome()
                    });
        }
    }

