    private void lstTimesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstTimesMouseClicked
        time = (Time) lstTimes.getSelectedValue();
        lblDirigente.setText(time.getDirigente());
        lblCidade.setText(time.getCidade());
        byte[] escudo = time.getEscudo();
        InputStream is = new ByteArrayInputStream(escudo);
        Image image = new ImageIcon(escudo).getImage();
        ImageIcon icon = new ImageIcon(image);
        lblEscudo.setIcon(icon);
    }//GEN-LAST:event_lstTimesMouseClicked

