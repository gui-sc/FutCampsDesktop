    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        time.setCampeonato(camp);
        time.setCabecaDeChave(ckhCabecaDeChave.isSelected());
        TimeDAO timeDAO = new TimeDAO();
        timeDAO.adicionarNoCamp(time);
        DefaultListModel model = (DefaultListModel) lstTimes.getModel();
        model.remove(lstTimes.getSelectedIndex());
        lblDirigente.setText("");
        lblCidade.setText("");
        lblEscudo.setIcon(escudoGenerico);
        atualizar();
    }//GEN-LAST:event_jButton3ActionPerformed

